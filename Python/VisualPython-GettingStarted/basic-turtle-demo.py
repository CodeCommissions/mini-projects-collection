import turtle

turtle.speed(1)
turtle.shape("turtle")
turtle.width(5)

turtle.color("blue")
turtle.forward(200)
turtle.left(90)
turtle.color("red")
turtle.forward(100)

turtle.penup()
turtle.goto(0,0)
turtle.pendown()
turtle.goto(100,100)
turtle.color("orange")
turtle.goto(-100,-100)


turtle.exitonclick()


























































